    public ArrayList<ShowAdmin> userList(){
     ArrayList<ShowAdmin>usersList=new ArrayList<>();
        try {
           
            con=DriverManager.getConnection("jdbc:mysql://localhost/CustomerDetails","root","");
            PreparedStatement pst1 = con.prepareStatement("select * from CustomerOrder");
            rs=pst1.executeQuery();
            ShowAdmin usr;
            while(rs.next()){
                
                usr=new ShowAdmin(
                        rs.getInt("Contacts"),
                        rs.getString("name"),
                        rs.getInt("order_id"),
                        rs.getInt("Quantity"));
               usersList.add(usr);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Shop.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return usersList;
}
public void show_Admin(){
          ArrayList<ShowAdmin> list = userList();
          DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
          Object[] row = new Object[4];
          for(int i=0;i<list.size();i++){
              row[0] = list.get(i).getConta();
              row[1] = list.get(i).getNm();
              row[2] = list.get(i).getId();
              row[3] = list.get(i).getQuant();
             
             
              model.addRow(row);
              jTable1.revalidate();
              jTable1.repaint();
              
          }
          
    
     }
